    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(String.valueOf(Slider1.getValue()));
        int b = Integer.parseInt(String.valueOf(Slider2.getValue()));
        int c = Integer.parseInt(String.valueOf(Slider3.getValue()));
        String Triangulo;
        
        if (a != 0 && b != 0 && c != 0) {
            if (a == b && b == c && c == a) {
                Triangulo = "Equil√°tero";
            }
            else if (a != c && b != c && b != a) {
                Triangulo = "Escaleno";
            } else {
                Triangulo = "Is√≥sceles";
            }
        } else {
            Triangulo = "Not";
        }
        
        switch(Triangulo) {
            case "Equil√°tero" -> result.setText("Tri√¢ngulo Equil√°tero");
            case "Is√≥sceles" -> result.setText("Tri√¢ngulo Is√≥sceles");
            case "Not" -> result.setText("N√£o √© um tri√¢ngulo");
            default -> result.setText("Tri√¢ngulo Escaleno");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

